def get_string(key, lang_code):
    """Get a translated string by key for the given language code.
    Falls back to English if the language or key is not found."""
    lang_data = LANGUAGES.get(lang_code, LANGUAGES["en"])
    return lang_data["strings"].get(key, LANGUAGES["en"]["strings"].get(key, key))


def get_language_name(lang_code):
    """Get the native name of a language by its code."""
    lang_data = LANGUAGES.get(lang_code)
    return lang_data["name"] if lang_data else lang_code


def get_translated_language_name(english_name, menu_lang_code):
    """Translate a language name (in English) into the menu language."""
    name_map = _LANGUAGE_NAMES.get(english_name)
    if name_map:
        return name_map.get(menu_lang_code, english_name)
    return english_name


def get_available_languages():
    """Return sorted list of (language_code, native_name) tuples."""
    return sorted(
        ((code, data["name"]) for code, data in LANGUAGES.items()),
        key=lambda x: x[1]
    )